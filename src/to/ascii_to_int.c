#include "../../include/lib42.h"

int	ascii_to_int(char *str)
{
    int		i;
    int		num;

    i = 0;
    num = 0;
    while (str[i])
    {
        num = num * 10 + str[i] - '0';
        i++;
    }
    return (num);
}